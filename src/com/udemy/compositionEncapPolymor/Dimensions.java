package com.udemy.compositionEncapPolymor;

public class Dimensions {
	private int width;
	private int length;
	private int height;

	public Dimensions(int width, int length,
			int height) {
		super();
		this.width = width;
		this.length = length;
		this.height = height;
	}

	public int getWidth() {
		return width;
	}

	public int getLength() {
		return length;
	}

	public int getHeight() {
		return height;
	}

}
